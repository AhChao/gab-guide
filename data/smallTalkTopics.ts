/**
 * Get a random topic from the specified level
 */
export const getRandomTopic = (level: TopicLevel): string => {
    const topics = smallTalkTopics[level];
    const randomIndex = Math.floor(Math.random() * topics.length);
    return topics[randomIndex];
};

/**
 * Get the label for a CEFR level
 */
export const getLevelLabel = (level: TopicLevel): string => {
    const labels: Record<TopicLevel, string> = {
        A: 'Beginner',
        B: 'Intermediate',
        C: 'Advanced',
    };
    return labels[level];
};
